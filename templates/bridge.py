async def _process_message(
    bot, chat_id: int, uid: int, prompt: str,
    session_id: str | None, save_session: bool = True,
    reply_func=None,
) -> None:
    """Core message processing — streams Claude response with live edits."""
    try:
        try:
            await bot.send_chat_action(chat_id, ChatAction.TYPING)
        except Exception:
            pass

        live_msg = None
        for _attempt in range(3):
            try:
                if reply_func:
                    live_msg = await reply_func("{{AGENT_EMOJI}} thinking...")
                else:
                    live_msg = await bot.send_message(chat_id, "{{AGENT_EMOJI}} thinking...")
                break
            except Exception:
                if _attempt < 2:
                    await asyncio.sleep(2)
        if not live_msg:
            await bot.send_message(chat_id, "Network error, please retry.")
            return
        live_msg_id = live_msg.message_id

        got_first_text = asyncio.Event()

        async def _keep_typing():
            while not got_first_text.is_set():
                try:
                    await bot.send_chat_action(chat_id, ChatAction.TYPING)
                except Exception:
                    pass
                try:
                    await asyncio.wait_for(got_first_text.wait(), timeout=5.0)
                    return
                except asyncio.TimeoutError:
                    pass

        typing_task = asyncio.create_task(_keep_typing())

        accumulated = ""
        last_edit_time = 0.0
        overflow_texts = []

        async for event in stream_query(prompt, session_id):
            event_type = event[0]

            if event_type == "session_id":
                if event[1] and save_session:
                    _set_session(uid, event[1])

            elif event_type == "tool":
                tool_name, tool_input = event[1], event[2]

                if tool_name == "Bash":
                    cmd = tool_input.get("command", "")
                    cmd_display = cmd[:80] + "..." if len(cmd) > 80 else cmd
                    status = f"{{{{AGENT_EMOJI}}}} running...\n`{cmd_display}`"
                elif tool_name in ("Read", "Glob", "Grep"):
                    target = tool_input.get("file_path") or tool_input.get("pattern") or ""
                    status = f"{{{{AGENT_EMOJI}}}} reading... `{target}`"
                elif tool_name in ("Edit", "Write"):
                    target = tool_input.get("file_path", "")
                    status = f"{{{{AGENT_EMOJI}}}} writing... `{target}`"
                else:
                    status = f"{{{{AGENT_EMOJI}}}} {tool_name}..."

                await _edit_msg(bot, chat_id, live_msg_id, status)
                last_edit_time = asyncio.get_event_loop().time()

            elif event_type == "text":
                if not got_first_text.is_set():
                    got_first_text.set()

                candidate = (accumulated + "\n" + event[1]).strip() if accumulated else event[1]

                if len(candidate) > MAX_MSG_LEN:
                    overflow_texts.append(event[1])
                else:
                    accumulated = candidate

                    now = asyncio.get_event_loop().time()
                    if now - last_edit_time >= STREAM_EDIT_INTERVAL:
                        await _edit_msg(bot, chat_id, live_msg_id, accumulated)
                        last_edit_time = now

            elif event_type == "done":
                break

        got_first_text.set()
        typing_task.cancel()

        if accumulated:
            await _edit_msg(bot, chat_id, live_msg_id, accumulated)
        else:
            await _edit_msg(bot, chat_id, live_msg_id, "(no response)")

        if overflow_texts:
            overflow_combined = "\n".join(overflow_texts)
            for chunk in split_message(overflow_combined):
                try:
                    await bot.send_message(chat_id, chunk)
                except Exception:
                    pass

    except Exception as e:
        log.exception("Error processing message")
        await bot.send_message(chat_id, f"Error: {e}")


