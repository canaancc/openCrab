async def stream_query(prompt: str, session_id: str | None = None):
    """Yield (event_type, data) tuples as Claude processes the query."""
    opts = ClaudeAgentOptions(
        system_prompt={"type": "preset", "preset": "claude_code", "append": APPEND_PROMPT},
        cwd=str(WORKSPACE),
        permission_mode="bypassPermissions",
        setting_sources=["project", "user"],
        max_turns=50,
    )
    if session_id:
        opts.resume = session_id

    async for msg in query(prompt=prompt, options=opts):
        if isinstance(msg, SystemMessage) and msg.subtype == "init":
            yield ("session_id", msg.data.get("session_id"))
        elif isinstance(msg, AssistantMessage):
            for block in msg.content:
                if isinstance(block, TextBlock):
                    yield ("text", block.text)
                elif isinstance(block, ToolUseBlock):
                    yield ("tool", block.name, block.input)
        elif isinstance(msg, ResultMessage):
            if hasattr(msg, "session_id") and msg.session_id:
                yield ("session_id", msg.session_id)
    yield ("done", None)


