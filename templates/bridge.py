def _parse_schedule(spec: str) -> str:
    """Parse schedule spec into cron expression.

    "30m" -> "*/30 * * * *", "1h" -> "0 * * * *", "6h" -> "0 */6 * * *",
    "1d" -> "0 0 * * *", "8:00" -> "0 8 * * *", "14:30" -> "30 14 * * *"
    """
    spec = spec.strip()
    m = re.fullmatch(r"(\d+)([mhd])", spec)
    if m:
        n, unit = int(m.group(1)), m.group(2)
        if unit == "m":
            return f"*/{n} * * * *"
        elif unit == "h":
            return "0 * * * *" if n == 1 else f"0 */{n} * * *"
        elif unit == "d":
            return "0 0 * * *"
    m = re.fullmatch(r"(\d{1,2}):(\d{2})", spec)
    if m:
        return f"{int(m.group(2))} {int(m.group(1))} * * *"
    raise ValueError(f"Unknown schedule format: {spec}. Use 30m/6h/1d or HH:MM")


def _read_crontab() -> str:
    result = subprocess.run(["crontab", "-l"], capture_output=True, text=True)
    return result.stdout if result.returncode == 0 else ""


def _write_crontab(content: str) -> None:
    subprocess.run(["crontab", "-"], input=content, text=True, check=True)


def _add_cron_entry(job_id: str, cron_expr: str) -> None:
    existing = _read_crontab()
    comment = f"# {CRON_COMMENT_PREFIX}{job_id}"
    cron_line = f"{cron_expr} cd {BOT_DIR} && {PYTHON} {CRON_RUNNER} --job-id {job_id} >> {BOT_DIR}/cron.log 2>&1"
    new_entry = f"{comment}\n{cron_line}\n"
    if existing and not existing.endswith("\n"):
        existing += "\n"
    _write_crontab(existing + new_entry)


def _remove_cron_entry(job_id: str) -> None:
    existing = _read_crontab()
    if not existing:
        return
    lines = existing.splitlines()
    filtered, skip_next = [], False
    for line in lines:
        if line.strip() == f"# {CRON_COMMENT_PREFIX}{job_id}":
            skip_next = True
            continue
        if skip_next:
            skip_next = False
            continue
        filtered.append(line)
    _write_crontab("\n".join(filtered) + "\n" if filtered else "")


async def cmd_every(update: Update, context) -> None:
    """Usage: /every <schedule> <prompt>"""
    uid = update.effective_user.id
    if uid not in ALLOWED_USERS:
        return
    args = update.message.text.split(None, 2)
    if len(args) < 3:
        await update.message.reply_text(
            "Usage: /every <schedule> <prompt>\n"
            "Schedule: 30m, 6h, 1d, or HH:MM\n"
            "Example: /every 8:00 morning briefing"
        )
        return
    schedule_spec, prompt = args[1], args[2]
    try:
        cron_expr = _parse_schedule(schedule_spec)
    except ValueError as e:
        await update.message.reply_text(str(e))
        return

    job_id = uuid.uuid4().hex[:12]
    job_def = {
        "prompt": prompt,
        "chat_id": update.effective_chat.id,
        "user_id": uid,
        "schedule": schedule_spec,
        "cron_expr": cron_expr,
    }
    _job_defs[job_id] = job_def
    _save_jobs()
    try:
        _add_cron_entry(job_id, cron_expr)
    except subprocess.CalledProcessError as e:
        del _job_defs[job_id]
        _save_jobs()
        await update.message.reply_text(f"Failed to write crontab: {e}")
        return
    await update.message.reply_text(
        f"Job {job_id[:8]} created.\nSchedule: {schedule_spec} ({cron_expr})\nPrompt: {prompt}"
    )


async def cmd_jobs(update: Update, _) -> None:
    uid = update.effective_user.id
    if uid not in ALLOWED_USERS:
        return
    if not _job_defs:
        await update.message.reply_text("No scheduled jobs.")
        return
    lines = [f"- {jid[:8]} | {jd['schedule']} ({jd.get('cron_expr','?')}) | {jd['prompt'][:50]}"
             for jid, jd in _job_defs.items()]
    await update.message.reply_text("Scheduled jobs:\n" + "\n".join(lines))


async def cmd_cancel(update: Update, context) -> None:
    uid = update.effective_user.id
    if uid not in ALLOWED_USERS:
        return
    args = update.message.text.split()
    if len(args) < 2:
        await update.message.reply_text("Usage: /cancel <job_id>")
        return
    target = args[1]
    matched = [jid for jid in _job_defs if jid.startswith(target)]
    if not matched:
        await update.message.reply_text(f"No job matching '{target}'.")
        return
    for jid in matched:
        _remove_cron_entry(jid)
        del _job_defs[jid]
        log.info("Cancelled job %s", jid[:8])
    _save_jobs()
    await update.message.reply_text(f"Cancelled {len(matched)} job(s).")


